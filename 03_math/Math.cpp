#include <iostream>
#include <cmath>

using namespace std;

int main () {
    // max
    cout << "Max: " << max(5, 10) << endl;
    // min 
    cout << "Min: " << min(1, 3) << endl;
    // square root 
    cout << "Akar dari 64: " << sqrt(64) << endl;
    // round 
    cout << "Pembulatan dari 2.3: " << round(2.3) << endl;
    // logarithm
    cout << "Hasil dari log 8: " << log(8) << endl;

    return 0;
}